CREATE TYPE subscription_status AS ENUM ('PENDING', 'ACTIVE', 'EXPIRED', 'CANCELLED', 'SUSPENDED');
CREATE TYPE billing_period AS ENUM ('MONTHLY', 'YEARLY');
CREATE TYPE discount_type AS ENUM ('PERCENTAGE', 'FIXED');
CREATE TYPE organization_feature_selection_status AS ENUM ('ACTIVE', 'INACTIVE');
CREATE TYPE exclusion_status AS ENUM ('ACTIVE', 'INACTIVE');
CREATE TYPE restriction_type AS ENUM ('BOOLEAN', 'NUMERIC', 'TEXT', 'DURATION');
CREATE TYPE restriction_operator AS ENUM ('EQ', 'NE', 'LT', 'LTE', 'GT', 'GTE', 'IN');
CREATE TYPE restriction_unit AS ENUM ('COUNT', 'DAY', 'MINUTE', 'FLAG', 'CHARACTER', 'PERCENT');
