-- IMS subscription and feature schema
-- Target: PostgreSQL 17+
--
-- Hybrid storage note:
-- 1. PostgreSQL stores organization, subscription, plan, feature catalog,
--    plan entitlements, plan selection rules, restrictions, organization
--    selections, coupons, employee/access taxonomy, and LDAP integration data.
-- 2. MongoDB stores the feature adjacency graph only.
-- 3. Organization-owned employee and access taxonomy stays table-driven,
--    not enum-driven, so each organization can define its own designations,
--    employment types, employment statuses, teams, resolver groups,
--    and permission groups.
-- 4. LDAP is treated as a first-class enterprise integration domain:
--    ldap_directory / ldap_group / ldap_user capture external directory state,
--    while mapping tables connect that state to the internal access model.
-- 5. Organization access assignment policy is explicit:
--    INTERNAL => direct employee assignment is primary
--    LDAP     => directory-derived assignment is primary
--    HYBRID   => LDAP-derived grants UNION manual grants MINUS employee-specific exclusions
--
-- Optional database creation (run separately as a superuser if needed):
-- CREATE DATABASE ims_subscription;
--
-- Then connect to the database before running the rest of this script.

-- Feature catalog examples:
-- 1  TKT_PAGE
-- 2  COMMENT_SECTION
-- 3  SUBJECT
-- 4  DESCRIPTION
-- 5  AUTO_TKT_RESOLUTION
-- 6  COMMENT_RES
-- 7  AI_SUBJECT_SUMMARY
-- 8  AUTH
-- 9  AUTH_TYPE_OAUTH
-- 10 AUTH_PROVIDER_GOOGLE
--
-- MongoDB adjacency examples:
-- 1  -> [2,3,4,5,7]
-- 2  -> [6]
-- 8  -> [9,11,12]
-- 9  -> [10,13,14]
-- 11 -> [15]

CREATE TYPE organization_status AS ENUM ('ACTIVE', 'INACTIVE', 'SUSPENDED');
